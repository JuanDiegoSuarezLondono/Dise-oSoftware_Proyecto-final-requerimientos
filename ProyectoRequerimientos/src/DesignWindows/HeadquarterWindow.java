    private void cbxCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbxCreateActionPerformed
        if(createActive == false){
            createActive = true;            
        }
        else {
            createActive = false;
        }        
        cbxList.setEnabled(!createActive);
        txtSectorName.setEnabled(createActive);
        txtHeadquarterID.setEnabled(createActive);
        btnDone.setEnabled(createActive);
    }//GEN-LAST:event_cbxCreateActionPerformed

    private void btnDoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoneActionPerformed
        Headquarter myHeadquarter = null;
        if(createActive == true){
            if(!txtSectorName.getText().trim().isEmpty() && !txtHeadquarterID.getText().trim().isEmpty()){
                company.addHeadquarters(new Headquarter(txtSectorName.getText().trim(),txtHeadquarterID.getText().trim()));
            }
        }
        else if(listActive == true){
            for (int i=0;i<company.getHeadquartersList().size();i++) {
                if (company.getHeadquartersList().get(i).getSectorName().equalsIgnoreCase(lstList.getSelectedValue())) {
                    myHeadquarter = company.getHeadquartersList().get(i);
                }
            }
            company.editHeadquarters(myHeadquarter, new Headquarter(txtSectorName.getText(), txtHeadquarterID.getText()));
            if(!company.getHeadquartersList().isEmpty()) {
                String[]  list = new String[company.getHeadquartersList().size()];
                list = company.listHeadquarters();
                lstList.setListData(list);
            }
            else {
                lstList.setListData(new String[]{""});
            }
        }
        try {
            rwo.write(new File("Bin","Company.bin"),false, company);
        } catch (IOException ex) {
            Logger.getLogger(HeadquarterWindow.class.getName()).log(Level.SEVERE, null, ex);
        }        
    }//GEN-LAST:event_btnDoneActionPerformed

    private void cbxListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbxListActionPerformed
        if(listActive == false){
            listActive = true; 
            try {
                rwo.read(new File("Bin","Company.bin"), company);
            } catch (IOException ex) {
                Logger.getLogger(HeadquarterWindow.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(HeadquarterWindow.class.getName()).log(Level.SEVERE, null, ex);
            }
            if(!company.getHeadquartersList().isEmpty()) {
                String[]  list = new String[company.getHeadquartersList().size()];
                list = company.listHeadquarters();
                lstList.setListData(list);
            }
            else {
                lstList.setListData(new String[]{""});
            }
        }
        else {
            listActive = false;
            lstList.setListData(new String[]{""});
            btnDone.setEnabled(listActive);
            btnDelete.setEnabled(listActive);
            txtSectorName.setEnabled(listActive);
            txtHeadquarterID.setEnabled(listActive);
        }
        cbxCreate.setEnabled(!listActive);
        lstList.setEnabled(listActive);        
    }//GEN-LAST:event_cbxListActionPerformed

    private void lstListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstListMouseClicked
        if(createActive == false) {
        Headquarter myHeadquarter = null;
        btnDone.setEnabled(listActive);
        btnDelete.setEnabled(listActive);
        txtSectorName.setEnabled(listActive);
        txtHeadquarterID.setEnabled(listActive);
        txtSectorName.setText(lstList.getSelectedValue());
        for (int i=0;i<company.getHeadquartersList().size();i++) {
            if (company.getHeadquartersList().get(i).getSectorName().equalsIgnoreCase(lstList.getSelectedValue())) {
                myHeadquarter = company.getHeadquartersList().get(i);
            }
        }
        txtHeadquarterID.setText(myHeadquarter.getIdHeadquarters());
        }
    }//GEN-LAST:event_lstListMouseClicked

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        Headquarter myHeadquarter = null;
        for (int i=0;i<company.getHeadquartersList().size();i++) {
            if (company.getHeadquartersList().get(i).getSectorName().equalsIgnoreCase(lstList.getSelectedValue())) {
                myHeadquarter = company.getHeadquartersList().get(i);
            }
        }
        company.deleteHeadquarters(myHeadquarter);
        try {
            rwo.write(new File("Bin","Company.bin"),false, company);
        } catch (IOException ex) {
            Logger.getLogger(HeadquarterWindow.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(!company.getHeadquartersList().isEmpty()) {
                String[]  list = new String[company.getHeadquartersList().size()];
                list = company.listHeadquarters();
                lstList.setListData(list);
            }
            else {
                lstList.setListData(new String[]{""});
            }
    }//GEN-LAST:event_btnDeleteActionPerformed

