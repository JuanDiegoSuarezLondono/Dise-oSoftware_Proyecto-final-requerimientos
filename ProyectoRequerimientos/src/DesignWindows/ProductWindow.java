    private void cbxListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbxListActionPerformed
        if(listActive == false){
            listActive = true; 
            try {
                rwo.read(new File("Bin","Company.bin"), company);
            } catch (IOException ex) {
                Logger.getLogger(HeadquarterWindow.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(HeadquarterWindow.class.getName()).log(Level.SEVERE, null, ex);
            }
            if(!company.getHeadquartersList().isEmpty()) {
                if (!myHeadquarter.getStock().getProductList().isEmpty()) {
                    String[]  list = new String[myHeadquarter.getStock().getProductList().size()];
                    list = myHeadquarter.getStock().listProduct();
                    lstList.setListData(list);
                }
            }
            else {
                lstList.setListData(new String[]{""});
            }
        }
        else {
            listActive = false;
            txtPrice.setEnabled(listActive);
            txtTechicianName.setEnabled(listActive);
            txtTechicianPercentage.setEnabled(listActive);  
            txtProductID.setEnabled(listActive);
            txtProductName.setEnabled(listActive); 
            lstList.setListData(new String[]{""});
            btnDone.setEnabled(listActive);
            btnDelete.setEnabled(listActive);            
        }
        cbxTechincalService.setEnabled(!listActive);
        lstList.setEnabled(listActive);
    }//GEN-LAST:event_cbxListActionPerformed

    private void lstListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstListMouseClicked
        Product myProduct = null;
        TechnicalService myTechnicalService = null;
        for (int i=0;i<myHeadquarter.getStock().getProductList().size();i++) {
            if (myHeadquarter.getStock().getProductList().get(i).getProductName().equalsIgnoreCase(lstList.getSelectedValue())) {
                myProduct = myHeadquarter.getStock().getProductList().get(i);
            }
        }
        if(myProduct.getIsTechnical()) {
            myTechnicalService = (TechnicalService) myProduct;
            txtPrice.setEnabled(listActive);
            txtTechicianName.setEnabled(listActive);
            txtTechicianPercentage.setEnabled(listActive);
            txtPrice.setText(String.valueOf(myTechnicalService.getPrice()));
            txtProductName.setText(myTechnicalService.getProductName());
            txtProductID.setText(String.valueOf(myTechnicalService.getIdProduct()));
            txtTechicianName.setText(myTechnicalService.getTechnicianName());
            txtTechicianPercentage.setText(String.valueOf(myTechnicalService.getTecnicPercentage()));            
        }
        else {
            txtPrice.setEnabled(!listActive);
            txtTechicianName.setEnabled(!listActive);
            txtTechicianPercentage.setEnabled(!listActive);
            txtProductName.setText(myProduct.getProductName());
            txtProductID.setText(String.valueOf(myProduct.getIdProduct()));
        }   
        txtProductID.setEnabled(listActive);
        txtProductName.setEnabled(listActive);
        btnDone.setEnabled(listActive);
        btnDelete.setEnabled(listActive);
        
    }//GEN-LAST:event_lstListMouseClicked

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        Product myProduct = null;
        for (int i=0;i<myHeadquarter.getStock().getProductList().size();i++) {
            if (myHeadquarter.getStock().getProductList().get(i).getProductName().equalsIgnoreCase(lstList.getSelectedValue())) {
                myProduct = myHeadquarter.getStock().getProductList().get(i);
            }
        }
        myHeadquarter.getStock().deleteProduct(myProduct);
        if(!company.getHeadquartersList().isEmpty()) {
                if (!myHeadquarter.getStock().getProductList().isEmpty()) {
                    String[]  list = new String[myHeadquarter.getStock().getProductList().size()];
                    list = myHeadquarter.getStock().listProduct();
                    lstList.setListData(list);
                }
            }
            else {
                lstList.setListData(new String[]{""});
            }
    }//GEN-LAST:event_btnDeleteActionPerformed

