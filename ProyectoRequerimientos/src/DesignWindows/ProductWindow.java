    private void btnDoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoneActionPerformed
        Product myProduct = null;
        Reference myReference = null;
        if(TechincalActive == true){
            if(!txtTechicianName.getText().trim().isEmpty() && !txtTechicianPercentage.getText().trim().isEmpty()){
                myHeadquarter.getStock().addPoduct(new TechnicalService(txtTechicianName.getText().trim(),Integer.parseInt(txtTechicianPercentage.getText().trim()), txtProductName.getText().trim(), Integer.parseInt(txtPrice.getText().trim()), Integer.parseInt(txtProductID.getText().trim()),true));
            }
        }
        else if(listActive == true){
            for (int i=0;i<myHeadquarter.getStock().getProductList().size();i++) {
                if (myHeadquarter.getStock().getProductList().get(i).getProductName().equalsIgnoreCase(lstList.getSelectedValue())) {
                    myProduct = myHeadquarter.getStock().getProductList().get(i);
                }
            }
            if(myProduct.getIsTechnical() == false) { 
                myReference = (Reference) myProduct;
                myHeadquarter.getStock().editProduct(myReference,new Reference(myReference.getNameReference(),myReference.getIdReference(),myReference.getAmount(),myReference.getBrandName(),myReference.getIdBrand(),txtProductName.getText(),myReference.getPrice(),Integer.parseInt(txtProductID.getText()),myReference.getIsTechnical()));
            }
            else {
                myHeadquarter.getStock().editProduct(myProduct,new TechnicalService(txtTechicianName.getText(), Integer.parseInt(txtTechicianPercentage.getText()), txtProductName.getText(), Integer.parseInt(txtPrice.getText()), Integer.parseInt(txtProductID.getText()), true));
            }
            if(!company.getHeadquartersList().isEmpty()) {
                if (!myHeadquarter.getStock().getProductList().isEmpty()) {
                    String[]  list = new String[myHeadquarter.getStock().getProductList().size()];
                    list = myHeadquarter.getStock().listProduct();
                    lstList.setListData(list);
                }
            }
            else {
                lstList.setListData(new String[]{""});
            }
        }
        try {
            rwo.write(new File("Bin","Company.bin"),false, company);
        } catch (IOException ex) {
            Logger.getLogger(HeadquarterWindow.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDoneActionPerformed

